window.i18n = i18n;
const DEFAULT_LANG = 'zh';

const themeKeyMap = {
    light: 'theme_light',
    dark: 'theme_dark',
    sunset: 'theme_sunset'
};

function setTheme(theme) {
    const themeName = Object.hasOwn(themeKeyMap, theme) ? theme : 'light';
    document.body.classList.remove('theme-light', 'theme-dark', 'theme-sunset');
    document.body.classList.add(`theme-${themeName}`);
    localStorage.setItem('preferredTheme', themeName);
    const themeSelect = document.getElementById('theme-select');
    if (themeSelect && themeSelect.value !== themeName) themeSelect.value = themeName;
}

function syncThemeSelectorLanguage(lang) {
    const themeSelect = document.getElementById('theme-select');
    if (!themeSelect) return;
    const dict = i18n[lang] || i18n[DEFAULT_LANG];
    themeSelect.setAttribute('aria-label', dict.theme_select_aria || i18n[DEFAULT_LANG].theme_select_aria);
    Array.from(themeSelect.options).forEach(option => {
        const key = themeKeyMap[option.value];
        option.textContent = (key && dict[key]) || option.value;
    });
}

function initThemeSelector() {
    const nav = document.querySelector('nav');
    if (!nav) return;

    let navRight = nav.querySelector('.nav-right');
    if (!navRight) {
        navRight = document.createElement('div');
        navRight.className = 'nav-right';
        const navLinks = nav.querySelector('.nav-links');
        if (navLinks) navRight.appendChild(navLinks);
        nav.appendChild(navRight);
    }

    let themeSelect = document.getElementById('theme-select');
    if (!themeSelect) {
        const wrapper = document.createElement('div');
        wrapper.className = 'theme-selector';
        themeSelect = document.createElement('select');
        themeSelect.id = 'theme-select';
        Object.keys(themeKeyMap).forEach(value => {
            const option = document.createElement('option');
            option.value = value;
            themeSelect.appendChild(option);
        });
        wrapper.appendChild(themeSelect);
        navRight.appendChild(wrapper);
    }

    if (!themeSelect.dataset.themeBound) {
        themeSelect.addEventListener('change', e => setTheme(e.target.value));
        themeSelect.dataset.themeBound = 'true';
    }
    syncThemeSelectorLanguage(localStorage.getItem('preferredLang') || DEFAULT_LANG);
    setTheme(localStorage.getItem('preferredTheme') || 'light');
}

function setLanguage(lang) {
    localStorage.setItem('preferredLang', lang);
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (i18n[lang][key]) el.textContent = i18n[lang][key];
    });
    if (document.getElementById('typewriter')) startTypewriter(lang);
    syncDetailButtonLanguage(lang);
    syncThemeSelectorLanguage(lang);
}

let typeInterval;
function startTypewriter(lang) {
    clearInterval(typeInterval);
    const typeEl = document.getElementById('typewriter');
    if (!typeEl) return;
    const texts = i18n[lang]["hero_sub"];
    let i = 0, j = 0, deleting = false;

    function type() {
        let current = texts[i];
        typeEl.innerHTML = current.substring(0, j) + '_';
        if (!deleting && j < current.length) j++;
        else if (deleting && j > 0) j--;
        else {
            deleting = !deleting;
            if (!deleting) i = (i + 1) % texts.length;
        }
    }

    typeInterval = setInterval(type, 150);
}

function initProjectFilter() {
    const filterButtons = document.querySelectorAll('.filter-btn');
    const cards = document.querySelectorAll('.project-card');
    if (!filterButtons.length || !cards.length) return;
    filterButtons.forEach(button => {
        button.addEventListener('click', () => {
            filterButtons.forEach(btn => btn.classList.remove('active'));
            button.classList.add('active');
            const filter = button.dataset.filter;
            cards.forEach(card => {
                const match = filter === 'all' || card.dataset.category === filter;
                card.style.display = match ? 'block' : 'none';
            });
        });
    });
}

function syncDetailButtonLanguage(lang) {
    document.querySelectorAll('.detail-toggle').forEach(btn => {
        const key = btn.dataset.i18n || 'show_more';
        if (i18n[lang][key]) btn.textContent = i18n[lang][key];
    });
}

function initProjectDetails() {
    const detailButtons = document.querySelectorAll('.detail-toggle');
    if (!detailButtons.length) return;
    detailButtons.forEach(btn => {
        btn.addEventListener('click', () => {
            const extra = btn.nextElementSibling;
            if (!extra) return;
            extra.classList.toggle('open');
            btn.dataset.i18n = extra.classList.contains('open') ? 'show_less' : 'show_more';
            const lang = localStorage.getItem('preferredLang') || 'zh';
            if (i18n[lang][btn.dataset.i18n]) btn.textContent = i18n[lang][btn.dataset.i18n];
        });
    });
}

function initScrollReveal() {
    if (typeof IntersectionObserver === 'undefined') return;
    const reveals = document.querySelectorAll('.reveal');
    if (!reveals.length) return;
    const observer = new IntersectionObserver(entries => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
                observer.unobserve(entry.target);
            }
        });
    }, { threshold: 0.12 });
    reveals.forEach(el => observer.observe(el));
}

function initBackToTop() {
    const topBtn = document.getElementById('to-top');
    if (!topBtn) return;
    topBtn.addEventListener('click', () => window.scrollTo({ top: 0, behavior: 'smooth' }));
    window.addEventListener('scroll', () => {
        topBtn.classList.toggle('show', window.scrollY > 300);
    });
}

function initContactInteractions() {
    const page = document.querySelector('.contact-page');
    if (!page) return;

    const status = document.getElementById('console-status');
    const tipEl = document.getElementById('maker-tip');
    const tips = [
        '系统在线：结构轻量、控制稳定、可快速迭代。',
        '灵感提示：先做可运行原型，再做精细化优化。',
        'Maker 思路：机械结构与控制参数要同步调优。',
        '工程习惯：每次迭代只改一个关键变量，便于验证。'
    ];

    document.querySelectorAll('.copy-btn').forEach(btn => {
        btn.addEventListener('click', async () => {
            const targetId = btn.dataset.copyTarget;
            const target = targetId ? document.getElementById(targetId) : null;
            if (!target) return;
            const content = target.textContent.trim();
            let copied = false;
            try {
                if (navigator.clipboard && navigator.clipboard.writeText) {
                    await navigator.clipboard.writeText(content);
                    copied = true;
                }
            } catch (err) {
                copied = false;
            }

            if (copied && status) {
                status.textContent = `状态：Copied · ${content}`;
            }

            if (copied) {
                btn.textContent = '已复制';
                setTimeout(() => {
                    btn.textContent = '复制';
                }, 1000);
            } else if (status) {
                status.textContent = '状态：复制失败，请手动复制';
            }
        });
    });

    const tipBtn = document.getElementById('tip-refresh');
    if (tipBtn && tipEl) {
        let lastTipIndex = -1;
        tipBtn.addEventListener('click', () => {
            let nextIndex = Math.floor(Math.random() * tips.length);
            if (tips.length > 1 && nextIndex === lastTipIndex) {
                nextIndex = (nextIndex + 1) % tips.length;
            }
            lastTipIndex = nextIndex;
            tipEl.textContent = tips[nextIndex];
            if (status) status.textContent = '状态：Console Updated';
        });
    }

    const themeBtn = document.getElementById('theme-toggle');
    if (themeBtn) {
        themeBtn.addEventListener('click', () => {
            const steel = page.classList.toggle('contact-steel');
            themeBtn.textContent = steel ? '切回简约模式' : '切换钢铁模式';
            if (status) status.textContent = steel ? '状态：Steel Mode Engaged' : '状态：Ready · Maker Mode';
        });
    }

    document.querySelectorAll('.signal-bar i').forEach(line => {
        const rawLevel = Number(line.dataset.level);
        const level = Number.isFinite(rawLevel) ? rawLevel : 0;
        requestAnimationFrame(() => {
            line.style.width = `${Math.max(0, Math.min(100, level))}%`;
        });
    });
}

window.onload = () => {
    initThemeSelector();
    const savedLang = localStorage.getItem('preferredLang') || DEFAULT_LANG;
    const langSelect = document.getElementById('lang-select');
    if (langSelect) {
        langSelect.value = savedLang;
    }
    setLanguage(savedLang);
    initProjectFilter();
    initProjectDetails();
    initScrollReveal();
    initBackToTop();
    initContactInteractions();
};
